
function add( first, second )
{
	return first + second;
}

function subtract( first, second )
{
	return first - second;
}

function multiply( first, second )
{
	return first * second;
}

function divide( first, second )
{
	return first / second;
}

function seeTypes( first, second )
{
	console.log( "Type of first is " + typeof( first ) );
	console.log( "Type of second is " + typeof( second ) );
}
